public class Conditionals {
    public static void main(String[] args) {
        int number = 2;
        if (number <= 0) {
            System.out.println("Not enough");
        }
        if (number == 1) {
            System.out.println("One");
        }
        if (number == 2) {
            System.out.println("Two");
        }
        if (number > 2) {
            System.out.println("A lot");
        }
    }
}
